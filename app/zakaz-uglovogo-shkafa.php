									<i class="fa fa-check" aria-hidden="true"></i><h4>Корпусный шкаф.</h4>
									<p>Корпусные угловые шкафы купе — это полноценная мебель, ее всегда можно разобрать, перенести в другой угол или новое помещение. От обычного шкафа они отличаются наличием раздвижных дверей и тем, что по высоте обычно занимают все пространство до потолка. С завода или цеха поставляются в разобранном виде, собираются по месту, так как имеют такие габариты, что в двери просто не пройдут.</p>
									<p>Минусом будет более высокая стоимость такого шкафа.</p>
