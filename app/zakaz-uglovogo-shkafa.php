									<i class="fa fa-check" aria-hidden="true"></i><h4>Треугольный угловой шкаф.</h4>
									<img class="img-responsive" src="images/article/uglovoj-shkaf2.jpg" alt="Картинка: Треугольный угловой шкаф">
									<p>Треугольный или диагональный: это бюджетный вариант среди угловых шкафов. Его плюсы удобство расположения в небольших помещениях, простота изготовления и при этом большая объемность.</p>
									<ul class="list-unstyled">Плюсы треугольного углового шкафа:
									 	<li><i class="fa fa-plus-circle"></i>компактность, быстрая сборка и простота монтажа;</li>
									 	<li><i class="fa fa-plus-circle"></i>удобны в небольших помещениях, позволяя использовать пространство между двух дверей или окон на прилегающих стенах;</li>
									 	<li><i class="fa fa-plus-circle"></i>дешевизна по отношению к другим формам угловых шкафов.</li>
									 </ul> 
									<ul class="list-unstyled">К минусам следует отнести:
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>малая функциональность полок и встроенных элементов;</li> 
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>глубина таких шкафов больше чем у прямолинейных аналогов и потому доступ к дальней угловой зоне может быть затруднен;</li>
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>визуальное уменьшение пространства помещения из-за среза углов.</li>
									</ul>
