					<ul class="text-justify list-unstyled"><h2 class="text-center">Разновидности угловых шкафов.</h2>
						<p class="text-center">Угловые шкафы различаются формой (треугольный, Г-образный и т.д.) и типом(встроенный или корпусный) </p>
						<hr>
						<li><h3 class="text-center accent-color">Тип шкафа</h3>
							<ul class="list-unstyled">
								<li>
									<i class="fa fa-check" aria-hidden="true"></i><h4>Встроенный шкаф.</h4>
									<p>В качестве стенок, пола и потолка в  встроенном варианте используются конструкции помещения. К ним крепится фасад с раздвижными дверями. Конструкция получается стационарной — направляющие крепятся к стенам, полу и потолку. Основной плюс этого типа угловых шкафов — экономичность, мы используем максимум пространства. Фактически нам нужна только площадь для установки дверей. Конструкция встроенного шкафа позволяет установку в самые нестандартные и малопригодные углы. </p>
									<p>Недостаток — невозможность переноса. Конструкция устанавливается один раз, на все существование. А в случае демонтажа встроенного шкафа, угол будет требовать ремонта.  Еще один важный момент: стены, пол и потолок должны быть ровными, иначе конструкция будет перекошенной, что негативно скажется на работе раздвижных дверей.</p>
									<p>Конструкция встроенных угловых шкафов в спальне может быть любой, согласно потребностям конкретной семьи. Она ограничивается только размерами отведенной под них площади и особенностями планировки.</p>
