									 	<li><i class="fa fa-plus-circle"></i>функциональность и удобство в пользовании;</li>
									 	<li><i class="fa fa-plus-circle"></i>возможность смягчения выпуклых углов;</li>
									 	<li><i class="fa fa-plus-circle"></i>достаточно компактен при своей вместимости.</li>
