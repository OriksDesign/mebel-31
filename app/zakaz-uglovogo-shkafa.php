									<i class="fa fa-check" aria-hidden="true"></i><h4>Трапециевидный угловой шкаф.</h4>
									<img class="img-responsive" src="images/article/uglovoj-shkaf3.jpg" alt="Картинка: Трапециевидный угловой шкаф">
									<p>Трапециевидный шкаф обладает большой вместительностью. Большой плюс таких шкафов – их легко комбинировать с остальной меблировкой спальни.</p>
									<p>Дополнительная стенка позволяет присоединить к трапециквидному шкафу еще какой-нибудь элемент мебели.</p>
									<p>Такой шкаф легко впишется в любой угол комнаты и будет гармонировать с остальной мебелью.</p>
									<ul class="list-unstyled">Плюсы трапециевидного углового шкафа:
									 	<li><i class="fa fa-plus-circle"></i>удобство и большая вместительность;</li>
									 	<li><i class="fa fa-plus-circle"></i>отлично гармонирует с остальным декором помещения;</li>
									 	<li><i class="fa fa-plus-circle"></i>возможность установки большого количества встроенных элементов.</li>
									 </ul> 
									<ul class="list-unstyled">К минусам следует отнести:
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>массивность влекущая за собой сложность сборки;</li> 
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>в основном не стандартные модели, что увеличивает цену.</li>
									</ul>
