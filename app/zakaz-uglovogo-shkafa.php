						<li><h3 class="text-center accent-color">Форма шкафа.</h3>
							<ul class="list-unstyled">
								<li>
									<i class="fa fa-check" aria-hidden="true"></i><h4>Пятиугольный угловой шкаф.</h4>
									<img class="img-responsive" src="images/article/uglovoj-shkaf1.jpg" alt="Картинка: Пятиугольный угловой шкаф">
									<p>Пятиугольный или пятистенный: самый массивный среди угловых шкафов. Благодаря своей вместительности входит в ТОП заказываемых.</p>
									<p>Модели шкафов-купе пятиугольной формы имеют универсальную схему сборки. Заказчик может самостоятельно определить количество полок и вешалок, а также способ их размещения.</p>
									<p>Пятиугольный шкаф при внушительной глубине корпуса не теряет своих функциональных качеств. В его огомном пространстве можно разместить множество элементов и полок. Такой шкаф станет достойным сопреником даже гардеробной комнате.</p>
									<ul class="list-unstyled">Плюсы пятиугольного углового шкафа:
									 	<li><i class="fa fa-plus-circle"></i>вместимость, разнообразие элементов ограничивает только Ваша фантазия;</li>
									 	<li><i class="fa fa-plus-circle"></i>функциональность и удобство в пользовании;</li>
									 	<li><i class="fa fa-plus-circle"></i>возможность грамотного позиционирования в пространстве помещения;</li>
									 	<li><i class="fa fa-plus-circle"></i>большое разнообразие типовых проектов пятиугольного шкафа, поможет при заказе.</li>
									 </ul> 
									<ul class="list-unstyled">К минусам следует отнести:
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>сложность сборки и установки;</li> 
									<li><i class="fa fa-minus-circle" aria-hidden="true"></i>громоздкость и большая глубина корпуса.</li>
									</ul>
